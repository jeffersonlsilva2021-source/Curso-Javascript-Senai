// VERIFICAR MAIORIDADE
// RECEBER UMA IDADE
// SE A IDADE FOR MAIOR OU IGUAL A 18 -> RETORNAR MAIOR DE IDADE
// SE NÃO FOR -> MENOR DE IDADE


const idade = 30
const nome = "João"
const sobrenome = "Silva"

if (idade >= 18) {
    // Se a condição for verdade, tudo aqui será executado!
    console.log(`Olá, ${nome} ${sobrenome}! Com ${idade} anos de idade você é de maior!!`)
} else {
    // se a condição for falsa, tudo aqui será executado!
    console.log(nome + sobrenome + ", com " + idade + " anos de idade, você é menor de idade!!")
}


